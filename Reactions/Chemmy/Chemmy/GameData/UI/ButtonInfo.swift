import Foundation
import UIKit

enum ResetButtonInfo {
    static let fontSize: CGFloat = 20
    static let width: CGFloat = 100
    static let height: CGFloat = 50
    static let topEdgeOffset: CGFloat = 50
    static let leadingEdgeOffset: CGFloat = 50
}

enum FireButtonInfo {
    static let fontSize: CGFloat = 20
    static let width: CGFloat = 100
    static let height: CGFloat = 50
    static let topEdgeOffset: CGFloat = 50
    static let leadingEdgeOffset: CGFloat = 200
}

enum RotateButtonInfo {
    static let scaleFactor: CGFloat = 2
    static let lateralEdgeOffset: CGFloat = 50
    static let longitudinalEdgeOffset: CGFloat = 80
}
